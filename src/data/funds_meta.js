/**
 * 根据一级分类获取二级分类列表
 * @param {string} l1Name - 一级分类名称
 * @returns {Array} 二级分类列表
 */
export function getL2Categories(l1Name) {
  const filtered = fundCategories.filter(item => item.l1 === l1Name)
  const l2Set = new Set()
  const result = []
  filtered.forEach(item => {
    if (!l2Set.has(item.l2)) {
      l2Set.add(item.l2)
      result.push({
        name: item.l2,
        key: item.l2_key,
        l1: item.l1
      })
    }
  })
  return result
}

/**
 * 根据一级和二级分类获取三级分类列表
 * @param {string} l1Name - 一级分类名称
 * @param {string} l2Name - 二级分类名称
 * @returns {Array} 三级分类列表
 */
export function getL3Categories(l1Name, l2Name) {
  return fundCategories
    .filter(item => item.l1 === l1Name && item.l2 === l2Name)
    .map(item => ({
      name: item.l3,
      key: item.l3_key,
      t2q: item.t2q,
      c6: item.c6,
      l1: item.l1,
      l2: item.l2
    }))
}

/**
 * 根据 t2q 获取分类信息
 * @param {string} t2q - 天天基金分类代码
 * @returns {Object|null} 分类信息
 */
export function getCategoryByT2Q(t2q) {
  return fundCategories.find(item => item.t2q === t2q) || null
}

/**
 * 获取所有属性标签列表
 * @returns {Array} 属性标签列表
 */
export function getAllC6Tags() {
  const tags = new Set()
  fundCategories.forEach(item => {
    if (item.c6 && item.c6.length > 0) {
      item.c6.forEach(tag => tags.add(tag))
    }
  })
  return Array.from(tags).sort()
}
